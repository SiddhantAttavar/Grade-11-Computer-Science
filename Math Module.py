# Import functions from the math module
from math import fabs, ceil, floor

# Print the absolute value
num = float(input("Enter a float: "))
print(f'Absolute Value: {fabs(num)}')

# Print the ceiling of the value
num = float(input("Enter a float: "))
print(f'Ceiling Value: {ceil(num)}')

# Print the floor of the value
num = float(input("Enter a float: "))
print(f'Ceiling Value: {floor(num)}')
